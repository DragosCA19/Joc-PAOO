    public void Draw(Graphics g) throws InterruptedException {
        for(int y = 0; y < refLink.GetGame().GetHeight()/Tile.TILE_HEIGHT; y++)
        {
            for(int x = 0; x < refLink.GetGame().GetWidth()/Tile.TILE_WIDTH; x++)
            {
                GetTile(x, y).Draw(g, (int)x * Tile.TILE_HEIGHT, (int)y * Tile.TILE_WIDTH);
            }
        }
        entityManager.Draw(g);
        if(incarcatNivel2==1) {
            g.setColor(Color.white);
            g.setFont(new Font("Arial", Font.BOLD, 30));
            g.drawString("Time: " + second, 0, 30);
        }
        if(second==0) {
            contor++;
            g.drawString("Game Over! :( ", 200, 200);
            if (contor == 3) {
                TimeUnit.SECONDS.sleep(2);
                System.exit(0);

            }
        }
    }
    //intoare o referinta catre dala din matricea de dale(harta)
    public Tile GetTile(int x, int y)
    {
        if(x < 0 || y < 0 || x >= width || y >= height)
        {
            return Tile.floorTile;
        }
        Tile t = Tile.tiles[tiles[x][y]];
        if(t == null)
        {
            return Tile.wallTile;
        }
        return t;
    }

    //Incarca Harta
    public void LoadWorld()
    {
        width=25;
        height=25;
        tiles=new int [width][height];
        for(int y=0;y<height;y++)
        {
            for(int x=0;x<width;x++)
            {
                tiles[x][y]=TutorialMap(y,x);
            }
        }
    }
